// 4.1: Площадь круга
const circleRadius = 5;
const circleArea = Math.PI * Math.pow(circleRadius, 2);
console.log("Площадь круга:", circleArea.toFixed(2));

// Завдання 4.2: Площадь прямоугольника
const length = 10;
const width = 4;
const rectangleArea = length * width;
console.log("Площа прямоугольника:", rectangleArea.toFixed(2));

// Завдання 4.3: Обьем цилиндра
const cylinderRadius = 3;
const height = 7;
const cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * height;
console.log("Обьем цилиндра:", cylinderVolume.toFixed(2));
