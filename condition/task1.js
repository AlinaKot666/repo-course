// 1. Create a variable averageGrade and assign a value
let averageGrade = 85;

// 2. We determine the level of success based on the assessment
if (averageGrade < 60) {
  console.log("very bed");       // Very bed
} else if (averageGrade <= 70) {
  console.log("Not so bed");         // Not so bed
} else if (averageGrade <= 80) {
  console.log("Good");               // Good
} else if (averageGrade <= 90) {
  console.log("Very good");          // Very good
} else if (averageGrade <= 100) {
  console.log("Excelent");            // Excelent
} else {
  console.log("incorrect assessment");  // incorrect assessment
}